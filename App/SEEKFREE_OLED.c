//内部使用用户无需调用
void OLED_WrDat(uint8 data)
{
	uint8 i=8;
	
	OLED_DC(1);
	OLED_SCL(0);
	while(i--)
	{
	  	if(data&0x80){OLED_SDA(1);}
	  	else{OLED_SDA(0);}
	  	OLED_SCL(1);
		
	  	OLED_SCL(0);
	  	data<<=1;
	}
}

//内部使用用户无需调用
void OLED_Wr6Dat(uint8 data)
{
	uint8 i=6;
	
	OLED_DC(1);
	OLED_SCL(0);
	while(i--)
	{
	 	if(data&0x80){OLED_SDA(1);}
	 	else{OLED_SDA(0);}
	 	OLED_SCL(1);
	 	OLED_SCL(0);
	 	data<<=1;
	}
}

//内部使用用户无需调用
void OLED_WrCmd(uint8 cmd)
{
	uint8 i=8;
	
	OLED_DC(0);
	OLED_SCL(0);;
	
	while(i--)
	{
		if(cmd&0x80){OLED_SDA(1);}
		else{OLED_SDA(0);}
		OLED_SCL(1);
		
		OLED_SCL(0);
		cmd<<=1;
	}
  
}

//内部使用用户无需调用
void OLED_DLY_ms(uint16 ms)
{
	uint16 a;
	while(ms)
	{
	 	a=16000;
	 	while(a--);
	 	ms--;
	}
	return;
}



